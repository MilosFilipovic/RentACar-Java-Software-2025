    private void btnKreirajZaposlenogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajZaposlenogActionPerformed
        
        String ime = txtNovozaposleniIme.getText();
        String prezime = txtNovozaposleniPrezime.getText();
        String korisnickoIme = txtNovozaposleniUsername.getText();
        String lozinka = txtNovozaposleniLozinka.getText();

        if (txtNovozaposleniIme.getText().isEmpty() || txtNovozaposleniPrezime.getText().isEmpty()
                || txtNovozaposleniUsername.getText().isEmpty() || txtNovozaposleniLozinka.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields must be filled!");
            return;
        }

        Zaposleni z = new Zaposleni(-1, ime, prezime, lozinka, korisnickoIme);

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.DODAJ_ZAPOSLENOG);
        kz.setParametar(z);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        boolean uspesno = (boolean) so.getOdgovor();

        if (uspesno) {
            JOptionPane.showMessageDialog(this, "Employee created!");
            
            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }
        
        
    }//GEN-LAST:event_btnKreirajZaposlenogActionPerformed

