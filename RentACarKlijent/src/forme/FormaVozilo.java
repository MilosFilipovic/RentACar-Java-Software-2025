    private void btnObrisiVoziloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiVoziloActionPerformed
        int row = tblVozila.getSelectedRow();

        if (row != -1) {
            TabelaVozilo mt = (TabelaVozilo) tblVozila.getModel();
            Object id = tblVozila.getValueAt(row, 0);
            mt.obrisiVozilo(row);

            

            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setOperacija(Operacije.OBRISI_VOZILO);
            kz.setParametar(id);

            Komunikacija.getInstance().posaljiZahtev(kz);
            ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

            mt.fireTableDataChanged();

            boolean obrisan = (boolean) so.getOdgovor();

            if (obrisan) {
                JOptionPane.showMessageDialog(this, "Vehicle deleted!");

                

            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Element not selected!");
        }
    }//GEN-LAST:event_btnObrisiVoziloActionPerformed

    private void btnIzmeniVoziloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniVoziloActionPerformed
        int row = tblVozila.getSelectedRow();
        if (row != -1) {
            TabelaVozilo tv = (TabelaVozilo) tblVozila.getModel();
            int id = (int) tblVozila.getValueAt(row, 0);
            String model = (String) tblVozila.getValueAt(row, 1);              // Ime je u koloni 1
            double cena = (Double) tblVozila.getValueAt(row, 2);          // Prezime je u koloni 2
            String karoserija = (String) tblVozila.getValueAt(row, 3);
            String konjaza = (String) tblVozila.getValueAt(row, 4);
            String kubikaza = (String) tblVozila.getValueAt(row, 5);
            String boja = (String) tblVozila.getValueAt(row, 6);

            DialogIzmeniVozilo div = new DialogIzmeniVozilo(this, true);
            
            div.postaviId(id);
            div.postaviModel(model);
            div.postaviCenu(cena);
            div.postaviKaroseriju(karoserija);
            div.postaviKonjazu(konjaza);
            div.postaviKubikazu(kubikaza);
            div.postaviBoju(boja);
            
            
            div.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "Element not selected!");
        }
    }//GEN-LAST:event_btnIzmeniVoziloActionPerformed

    private void btnPretraziVoziloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziVoziloActionPerformed
        if(txtPretraziVozilo.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter vehicle!");
            return;
        }
        
        String model = txtPretraziVozilo.getText();
        
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.PRETRAZI_VOZILA);
        kz.setParametar(model);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        ArrayList<Vozilo> pretraziV = (ArrayList<Vozilo>) so.getOdgovor();

        tblVozila.removeAll();

         
        TabelaVozilo tv = (TabelaVozilo) tblVozila.getModel();
        tv.setPretragaVozila(pretraziV);
    }//GEN-LAST:event_btnPretraziVoziloActionPerformed

