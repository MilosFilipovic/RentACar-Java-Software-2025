    private void btnSacuvajIzmenjenoMestoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajIzmenjenoMestoActionPerformed
        
        if (txtIzmenjenoMesto.getText().isEmpty() || txtIzmenjenaAdresa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields must be filled!");
            return;
        }
        
        int id = Integer.parseInt(lblIDMesta.getText()); 
        String naziv = txtIzmenjenoMesto.getText();
        String adresa = txtIzmenjenaAdresa.getText();
        

        Mesto mesto = new Mesto(id, naziv, adresa);
        
        

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.IZMENI_MESTO);
        kz.setParametar(mesto);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        boolean izmenjen = (boolean) so.getOdgovor();

        if (izmenjen) {
            JOptionPane.showMessageDialog(this, "Place is edited!");
            
            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }
    }//GEN-LAST:event_btnSacuvajIzmenjenoMestoActionPerformed

