    private void btnSacuvajIzmeneZaposlenogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajIzmeneZaposlenogActionPerformed
        
        if (txtIzmeniImeZaposlenog.getText().isEmpty() || txtIzmeniPrezimeZaposlenog.getText().isEmpty()
                || txtIzmeniUsernameZaposlenog.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields must be filled!");
            return;
        }
        
        int id = Integer.valueOf(lbID.getText());
        String ime = txtIzmeniImeZaposlenog.getText();
        String prezime = txtIzmeniPrezimeZaposlenog.getText();
        String korisnickoIme = txtIzmeniUsernameZaposlenog.getText();

        Zaposleni zaposlen = new Zaposleni(id, ime, prezime, null, korisnickoIme);
        
        

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.IZMENI_ZAPOSLENOG);
        kz.setParametar(zaposlen);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        boolean izmenjen = (boolean) so.getOdgovor();

        if (izmenjen) {
            JOptionPane.showMessageDialog(this, "Employee is edited!");
            
            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }
        
    }//GEN-LAST:event_btnSacuvajIzmeneZaposlenogActionPerformed

