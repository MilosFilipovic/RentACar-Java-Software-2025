    private void btnKreirajVoziloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajVoziloActionPerformed
        
        if (txtNovoVoziloBoja.getText().isEmpty() || txtNovoVoziloCM.getText().isEmpty()
                || txtNovoVoziloKonji.getText().isEmpty() || txtNovoVoziloModel.getText().isEmpty() || txtNovoVoziloType.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields must be filled!");
            return;
        }
        
        
        String model = txtNovoVoziloModel.getText();
        double cena = Double.parseDouble(ftxtNovoVoziloCena.getText());
        String karoserija = txtNovoVoziloType.getText();
        String konjaza = txtNovoVoziloKonji.getText();
        String kubikaza = txtNovoVoziloCM.getText();
        String boja = txtNovoVoziloBoja.getText();

        
        Vozilo v = new Vozilo(-1, model, cena, karoserija, konjaza, kubikaza, boja);

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.DODAJ_VOZILO);
        kz.setParametar(v);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        boolean uspesno = (boolean) so.getOdgovor();

        if (uspesno) {
            JOptionPane.showMessageDialog(this, "Vehicle created!");
            
            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }
    }//GEN-LAST:event_btnKreirajVoziloActionPerformed

