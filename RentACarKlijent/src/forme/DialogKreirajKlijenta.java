    private void popuniAdrese() {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_CB_ADRESA);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        ArrayList<Mesto> adrese = (ArrayList<Mesto>) so.getOdgovor();

        cmbAdrese.removeAllItems();

        for (Mesto m : adrese) {
            cmbAdrese.addItem(m);
        }
    }
