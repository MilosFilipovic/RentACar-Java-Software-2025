    private void btnKreirajNovogKlijentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajNovogKlijentaActionPerformed
        
        
        if (txtNoviKlijentIme.getText().isEmpty() || txtNoviKlijentPrezime.getText().isEmpty()
                || txtNoviKlijentBroj.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields must be filled!");
            return;
        }
        
        String ime = txtNoviKlijentIme.getText();
        String prezime = txtNoviKlijentPrezime.getText();
        String broj = txtNoviKlijentBroj.getText();
        Mesto mesto = (Mesto) cmbAdrese.getSelectedItem();
        int mestoID = mesto.getIdMesta();
        

        Klijent k = new Klijent(-1, ime, prezime, broj, mestoID);

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.DODAJ_KLIJENTA);
        kz.setParametar(k);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        boolean uspesno = (boolean) so.getOdgovor();

        if (uspesno) {
            JOptionPane.showMessageDialog(this, "Client created!");
            
            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }
    }//GEN-LAST:event_btnKreirajNovogKlijentaActionPerformed

