    private void btnObrisiZaposlenogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiZaposlenogActionPerformed
        int row = tblZaposleni.getSelectedRow();

        if (row != -1) {
            TabelaZaposleni mt = (TabelaZaposleni) tblZaposleni.getModel();
            Object id = tblZaposleni.getValueAt(row, 0);
            mt.obrisiZaposlenog(row);

            

            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setOperacija(Operacije.OBRISI_ZAPOSLENOG);
            kz.setParametar(id);

            Komunikacija.getInstance().posaljiZahtev(kz);
            ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

            mt.fireTableDataChanged();

            boolean obrisan = (boolean) so.getOdgovor();

            if (obrisan) {
                JOptionPane.showMessageDialog(this, "Employee deleted!");

                

            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Element not selected!");
        }

        
        
    }//GEN-LAST:event_btnObrisiZaposlenogActionPerformed

    private void btnIzmeniZaposlenogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniZaposlenogActionPerformed
        int row = tblZaposleni.getSelectedRow();
        if (row != -1) {
            
            int id = (int) tblZaposleni.getValueAt(row, 0);
            String ime = (String) tblZaposleni.getValueAt(row, 1);              // Ime je u koloni 1
            String prezime = (String) tblZaposleni.getValueAt(row, 2);          // Prezime je u koloni 2
            String korisnickoIme = (String) tblZaposleni.getValueAt(row, 3);    // Korisničko ime je u koloni 3

            DialogIzmeniZaposlenog diz = new DialogIzmeniZaposlenog(this, true);
            
            diz.postaviId(id);
            diz.postaviIme(ime);
            diz.postaviPrezime(prezime);
            diz.postaviKorisnickoIme(korisnickoIme);
            
            
            diz.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "Element not selected!");
        }

        
    }//GEN-LAST:event_btnIzmeniZaposlenogActionPerformed

