    private void btnPretragaZaposlenogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretragaZaposlenogActionPerformed
        
        if(txtUnesiImeZaposlenog.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter employee!");
            return;
        }
        
        String ime = txtUnesiImeZaposlenog.getText();
        
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.PRETRAZI_ZAPOSLENE);
        kz.setParametar(ime);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        ArrayList<Zaposleni> pretrazeniZ = (ArrayList<Zaposleni>) so.getOdgovor();

        tblZaposleni.removeAll();

         
        TabelaZaposleni tz = (TabelaZaposleni) tblZaposleni.getModel();
        tz.setPretragaZaposleni(pretrazeniZ);
        
    }//GEN-LAST:event_btnPretragaZaposlenogActionPerformed

