    private void btnSacuvajIzmenjenogKlijentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajIzmenjenogKlijentaActionPerformed
        
        if (txtIzmenjenogKlijentaIme.getText().isEmpty() || txtIzmenjenogKlijentaPrezime.getText().isEmpty()
                || txtIzmenjenogKlijentaBroj.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields must be filled!");
            return;
        }
        
        int id = Integer.parseInt(lblIDKlijenta.getText());
        String ime = txtIzmenjenogKlijentaIme.getText();
        String prezime = txtIzmenjenogKlijentaPrezime.getText();
        String telefon = txtIzmenjenogKlijentaBroj.getText();
        int mestoID = Integer.parseInt(txtIzmenjenogKlijentaGrad.getText());

        Klijent k = new Klijent(id, ime, prezime, telefon, mestoID);
        
        

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.IZMENI_KLIJENTA);
        kz.setParametar(k);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        boolean izmenjen = (boolean) so.getOdgovor();

        if (izmenjen) {
            JOptionPane.showMessageDialog(this, "Employee is edited!");
            
            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }
    }//GEN-LAST:event_btnSacuvajIzmenjenogKlijentaActionPerformed

