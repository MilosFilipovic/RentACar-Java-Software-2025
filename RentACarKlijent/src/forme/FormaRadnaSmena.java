    private void btnKreirajSmenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajSmenuActionPerformed
       
        
        if (ftxtNovaSmenaPocetak.getText().isEmpty() || ftxtNovaSmenaKraj.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields must be filled!");
            return;
        }

        LocalTime pocetakNove = LocalTime.parse(ftxtNovaSmenaPocetak.getText());
        LocalTime krajNove = LocalTime.parse(ftxtNovaSmenaKraj.getText());
        
        
        RadnaSmena rs = new RadnaSmena(-1, pocetakNove, krajNove);

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.DODAJ_RS);
        kz.setParametar(rs);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        boolean uspesno = (boolean) so.getOdgovor();

        if (uspesno) {
            JOptionPane.showMessageDialog(this, "New shift created!");
            
            
            
        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }
    }//GEN-LAST:event_btnKreirajSmenuActionPerformed

