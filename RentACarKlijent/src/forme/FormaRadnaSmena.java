    private void btnObrisiSmenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiSmenuActionPerformed
        
        int row = tblRadnaSmena.getSelectedRow();

        if (row != -1) {
            TabelaRadnaSmena mt = (TabelaRadnaSmena) tblRadnaSmena.getModel();
            Object id = tblRadnaSmena.getValueAt(row, 0);
            mt.obrisiSmenu(row);

            

            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setOperacija(Operacije.OBRISI_RS);
            kz.setParametar(id);

            Komunikacija.getInstance().posaljiZahtev(kz);
            ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

            mt.fireTableDataChanged();

            boolean obrisan = (boolean) so.getOdgovor();

            if (obrisan) {
                JOptionPane.showMessageDialog(this, "Shift deleted!");

                

            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Element not selected!");
        }
    }//GEN-LAST:event_btnObrisiSmenuActionPerformed

