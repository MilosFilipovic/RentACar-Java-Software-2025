    private void btnIzmeniSmenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniSmenuActionPerformed
        
        int row = tblRadnaSmena.getSelectedRow();
        if (row != -1) {
            
            int id = (int) tblRadnaSmena.getValueAt(row, 0);
            LocalTime pocetak =  (LocalTime) tblRadnaSmena.getValueAt(row, 1);              // pocetak smene kolona 1
            LocalTime kraj =  (LocalTime) tblRadnaSmena.getValueAt(row, 2);          // kraj smene kolona 2
                

            DialogIzmeniSmenu dirs = new DialogIzmeniSmenu(this, true);
            
            dirs.postaviId(id);
            dirs.postaviPocetak(pocetak);
            dirs.postaviKraj(kraj);
            
            
            
            dirs.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "Element not selected!");
        }
    }//GEN-LAST:event_btnIzmeniSmenuActionPerformed

    private void btnPretraziSmeneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziSmeneActionPerformed
        if(ftxtPocetakSmene.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter time!");
            return;
        }
        
        
        String pocetak = ftxtPocetakSmene.getText();
        
        
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.PRETRAZI_SMENE);
        kz.setParametar(pocetak);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        ArrayList<RadnaSmena> pretrazeneRS = (ArrayList<RadnaSmena>) so.getOdgovor();

        tblRadnaSmena.removeAll();

         
        TabelaRadnaSmena trs = (TabelaRadnaSmena) tblRadnaSmena.getModel();
        trs.setPretragaRS(pretrazeneRS);
    }//GEN-LAST:event_btnPretraziSmeneActionPerformed

