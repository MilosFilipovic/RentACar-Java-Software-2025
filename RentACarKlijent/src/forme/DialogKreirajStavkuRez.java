    private void btnNapraviStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNapraviStavkuActionPerformed
        
        if (txtBrojDana.getText().isEmpty() || txtNapomena.getText().isEmpty()
                || ftxtIznosVozila.getText().isEmpty() || ftxtIznosStavke.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields must be filled!");
            return;
        }
        
        Vozilo vozilo = (Vozilo) cmbVozila.getSelectedItem();
        String napomena = txtNapomena.getText();
        double cena = (double) ftxtIznosVozila.getValue();
        int dani = Integer.parseInt(txtBrojDana.getText());
        double iznosStavke = (double) ftxtIznosStavke.getValue();
        int voziloID = vozilo.getIdVozilo();
        int idRez = Integer.valueOf(lblRezID.getText());
        
        
        StavkaRezervacije stavka = new StavkaRezervacije( idRez, -1, vozilo.getModel(), napomena, cena, dani, iznosStavke, voziloID, vozilo);
        
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.KREIRAJ_STAVKU_REZ);
        kz.setParametar(stavka);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        boolean uspesno = (boolean) so.getOdgovor();

        if (uspesno) {
            JOptionPane.showMessageDialog(this, "Item created!");
            
            

        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }
        
    }//GEN-LAST:event_btnNapraviStavkuActionPerformed

