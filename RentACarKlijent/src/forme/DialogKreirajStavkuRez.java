    private void btnIzracunajIznosStavkeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzracunajIznosStavkeActionPerformed
        
        if(ftxtIznosVozila.getText().isEmpty() || txtBrojDana.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Fill the fields!");
            return;
        }
        
        double cena = Double.valueOf(ftxtIznosVozila.getText());
        int dani = Integer.parseInt(txtBrojDana.getText());
        ftxtIznosStavke.setValue(dani*cena);
        
    }//GEN-LAST:event_btnIzracunajIznosStavkeActionPerformed

