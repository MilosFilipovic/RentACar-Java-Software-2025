    private void btnKreirajRezActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajRezActionPerformed
        
        if (ftxtDatumPreuzimanja.getText().isEmpty() || ftxtDatumVracanja.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields must be filled!");
            return;
        }

        LocalDate uzimanje = LocalDate.parse(ftxtDatumPreuzimanja.getText());
        LocalDate vracanje = LocalDate.parse(ftxtDatumVracanja.getText());
        int zaposleniID = Integer.valueOf(lblZaposleniID.getText());
        
        Klijent k = (Klijent) cmbKlijenti.getSelectedItem();
        int klijentID = k.getIdKlijent();
        
        Rezervacija rez = new Rezervacija(-1, uzimanje, vracanje, 0, klijentID, k, zaposleniID, null);
        
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.KREIRAJ_REZERVACIJU);
        kz.setParametar(rez);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        boolean uspesno = (boolean) so.getOdgovor();

        if (uspesno) {
            JOptionPane.showMessageDialog(this, "Reservation created!");
            
            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }
        
        trenutnaRezervacija = rez;
    }//GEN-LAST:event_btnKreirajRezActionPerformed

