    private void btnObrisiMestoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiMestoActionPerformed
        int row = tblMesta.getSelectedRow();

        if (row != -1) {
            TabelaMesto tm = (TabelaMesto) tblMesta.getModel();
            Object id = tblMesta.getValueAt(row, 2);
            tm.obrisiSmenu(row);

            

            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setOperacija(Operacije.OBRISI_MESTO);
            kz.setParametar(id);

            Komunikacija.getInstance().posaljiZahtev(kz);
            ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

            tm.fireTableDataChanged();

            boolean obrisan = (boolean) so.getOdgovor();

            if (obrisan) {
                JOptionPane.showMessageDialog(this, "Place deleted!");

                

            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Element not selected!");
        }
    }//GEN-LAST:event_btnObrisiMestoActionPerformed

    private void btnIzmeniMestoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniMestoActionPerformed
        int row = tblMesta.getSelectedRow();
        if (row != -1) {
            TabelaMesto tm = (TabelaMesto) tblMesta.getModel();
            int id = (int) tblMesta.getValueAt(row, 2);
            String mesto = (String) tblMesta.getValueAt(row, 0);              // mesto je u koloni 0
            String adresa = (String) tblMesta.getValueAt(row, 1);          // adresa je u koloni 1
            

            DialogIzmeniMesto dim = new DialogIzmeniMesto(this, true);
            
            
            dim.postaviMesto(mesto);
            dim.postaviAdresu(adresa);
            dim.postaviID(id);
            
            
            
            dim.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "Element not selected!");
        }
    }//GEN-LAST:event_btnIzmeniMestoActionPerformed

    private void btnPretraziMestoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziMestoActionPerformed
        if(txtPretraziMesto.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter place!");
            return;
        }
        
        String grad = txtPretraziMesto.getText();
        
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.PRETRAZI_MESTA);
        kz.setParametar(grad);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        ArrayList<Mesto> pretrazenoMesto = (ArrayList<Mesto>) so.getOdgovor();

        tblMesta.removeAll();

         
        TabelaMesto tm = (TabelaMesto) tblMesta.getModel();
        tm.setPretragaMesto(pretrazenoMesto);
    }//GEN-LAST:event_btnPretraziMestoActionPerformed

    private void btnPretraziMestoPoIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziMestoPoIDActionPerformed
        if(txtPretragaPoID.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter place ID!");
            return;
        }
        
        int id = Integer.parseInt(txtPretragaPoID.getText());
        
        
        
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.PRETRAZI_IDMESTA);
        kz.setParametar(id);

        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();

        
        
        ArrayList<Mesto> pretraziM = (ArrayList<Mesto>) so.getOdgovor();

        tblMesta.removeAll();

        
         
        TabelaMesto tm = (TabelaMesto) tblMesta.getModel();
        tm.setPretragaMesto(pretraziM);
    }//GEN-LAST:event_btnPretraziMestoPoIDActionPerformed

